    int Login(){
        String UserName="";
        String Password="";
        String sql="SELECT * FROM ACCOUNT WHERE STATUS = TRUE";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){ UserName=rs.getString(1);Password=rs.getString(2);}
            else {JOptionPane.showMessageDialog(null,"Account Logged in Not recognized");return 0;}
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
            return 0;
        }
        JTextField text=new JTextField(10);
        JPasswordField Pswd=new JPasswordField(20);
        text.setText(UserName);
        text.setEditable(false);
        Pswd.setText("");
        JPanel panel = new JPanel(new GridLayout(0,2));
        panel.add(new JLabel("UserName : "));
        panel.add(text);
        panel.add(new JLabel("Password : "));
        panel.add(Pswd);
        int result=JOptionPane.showConfirmDialog(null,panel,"Confimation",JOptionPane.OK_CANCEL_OPTION);
        if(result==JOptionPane.OK_OPTION){
            if(Password.equals(String.valueOf(Pswd.getPassword()))) 
                {JOptionPane.showMessageDialog(null,"Are you SURE you want to SAVE THE CHANGES");
                    return 1;}
            else {
                JOptionPane.showMessageDialog(null,"Wrong Password ");
                return 0;
            }
        }
        else return 0;
    }
    int UpdateStudentBase(){
        String ScholarNo1=jtxtScholarNo.getText();
        String Name=jtxtStudentName.getText();
        String Branch=String.valueOf(jcomboBranch.getSelectedItem());
        String Semester=jtxtSem.getText();
        try {
            java.sql.Date DOB= new  java.sql.Date(new SimpleDateFormat("yyyy-MM-dd").parse(jtxtDOB.getText()).getTime());
            java.sql.Date Start=new  java.sql.Date(new SimpleDateFormat("yyyy-MM-dd").parse(jtxtStart.getText()).getTime());
            java.sql.Date End=new  java.sql.Date(new SimpleDateFormat("yyyy-MM-dd").parse(jtxtEnd.getText()).getTime());
            String sql="UPDATE STUDENT SET SCHOLARNO = '"+ScholarNo1+"' ,STUDENTNAME = '"+Name+"' ,"
                    + " BRANCH = '"+Branch+"' , SEMESTER = "+Semester+" , DOB = '"+DOB+"' ,"
                    + " MEM_START = '"+Start+"' , MEM_END = '"+End+"' WHERE SCHOLARNO = '"+ScholarNo+"'";
            conn.prepareStatement(sql).executeUpdate();
            sql="UPDATE BOOK SET SCHOLARNO = '"+ScholarNo1+"' WHERE SCHOLARNO = '"+ScholarNo+"'";
            conn.prepareStatement(sql).executeUpdate();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
            return 0;
        }
        ScholarNo=ScholarNo1;
        return 1;
    }
