    private void jbtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSearchActionPerformed
        ScholarNo=jtxtScholarNo.getText();
        String sql="SELECT * FROM STUDENT WHERE SCHOLARNO = '"+ScholarNo+"'";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                jtxtStudentName.setText(rs.getString(2));
                jtxtGender.setText(rs.getString(11));
                jcomboBranch.setSelectedItem(rs.getString(3));
                jtxtSem.setText(rs.getString(4));
                jtxtDOB.setText(rs.getString(5));
                jtxtStart.setText(rs.getString(6));
                jtxtEnd.setText(rs.getString(7));
                jtxtBook1.setText(rs.getString(8));
                jtxtBook2.setText(rs.getString(9));
                jtxtBook3.setText(rs.getString(10));
            }
            else{
                JOptionPane.showMessageDialog(null,"Student not found in Records");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jbtnSearchActionPerformed

