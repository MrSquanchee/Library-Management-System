    final void FillUserName(){
        String sql="SELECT * FROM ACCOUNT WHERE STATUS = TRUE";
        try{
            rs=conn.prepareStatement(sql).executeQuery();
            if(rs.next()){
                jtxtUserName.setText(LoggedUser=rs.getString("USERNAME"));
                jtxtName.setText(rs.getString("FULLNAME"));
            }
            else{
                JOptionPane.showMessageDialog(null,"Cannot Access account info");
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    ArrayList<User> getList(){
        ArrayList<User> List=new ArrayList<>();
        User user;
        String sql="SELECT * FROM ACCOUNT";
        try{
            rs=conn.prepareStatement(sql).executeQuery();
            while(rs.next()){
                user=new User(rs.getString("USERNAME"),rs.getString("FULLNAME"));
                List.add(user);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
        return List;
    }
    final void FillTable(){
        ArrayList<User> List=getList();
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        Object[] row=new Object[3];
        for(int i=0;i<List.size();i++){
            row[0]=List.get(i).getUserName();
            row[1]=List.get(i).getFullName();
            if(String.valueOf(row[0]).equals(LoggedUser)) row[2]=true;
            else row[2]=false;
            model.addRow(row);
        }
    }
