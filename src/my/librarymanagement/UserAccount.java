    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
       String SelectedUser=jtxtSelectedUser.getText();
       String Password,sql="SELECT * FROM ACCOUNT WHERE STATUS= TRUE";
       try{
           rs=conn.prepareStatement(sql).executeQuery();
           if(rs.next()){
               Password=rs.getString("PASSWORD");
           }else{
               JOptionPane.showMessageDialog(null,"Cannot Access ACCOUNT INFO");
               return ;
           }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null,e);
           return ;
       }
       if(SelectedUser.equals("")){ JOptionPane.showMessageDialog(null,"Select User");return;}
       if(SelectedUser.equals(jtxtUserName.getText())) {JOptionPane.showMessageDialog(null,"Cannot Perform this !!");return;}
       JTextField User=new JTextField();
       User.setText(jtxtUserName.getText());
       JPasswordField Pswd=new JPasswordField();
       Pswd.setText("");
       JPanel Panel=new JPanel(new GridLayout(0,2));
       Panel.add(new JLabel("User Name : "));
       Panel.add(User);
       Panel.add(new JLabel("Enter Password :"));
       Panel.add(Pswd);
       int result=JOptionPane.showConfirmDialog(null,Panel,"Confirm It's You ",JOptionPane.OK_CANCEL_OPTION);
       if(result==JOptionPane.OK_OPTION){
           if(String.valueOf(Pswd.getPassword()).equals(Password)){
               JOptionPane.showMessageDialog(null,"Are You Sure ???");
               try{
                    conn.prepareStatement("DELETE FROM ACCOUNT WHERE USERNAME='"+SelectedUser+"'").executeUpdate();
                    JOptionPane.showMessageDialog(null,"Account Deleted Successfully");
                    DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
                    model.setRowCount(0);
                    FillTable();
                    jtxtSelectedUser.setText("");
                    jtxtSelectedName.setText("");
               }catch(Exception w){
                  JOptionPane.showMessageDialog(null,w);
               }
           }else{
               JOptionPane.showMessageDialog(null,"Wrong Password");
           }
       }else{
           JOptionPane.showMessageDialog(null,"Wrong Password");
       }
    }//GEN-LAST:event_jbtnDeleteActionPerformed

