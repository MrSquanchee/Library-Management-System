    private void jbtnCreateStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCreateStudentActionPerformed
        // TODO add your handling code here:
        //StudentID here stores value for Student's Scholar No.
        String StudentID=jtxtStudentID.getText();
        String FullName=jtxtStudentFullName.getText();
        String Gender=jtxtGender.getSelectedItem().toString();
        String Branch=jtxtBranch.getSelectedItem().toString();
        int Semester=Integer.parseInt(jtxtSemester.getSelectedItem().toString());
        if(StudentID.length()!=8){ JOptionPane.showMessageDialog(null,"ScholarNo must be like YEAR+U/P+BRANCHCODE+3-DIGIT ROLLNO");return ;}
        java.sql.Date DOB;
        java.sql.Date StartDate;
        java.sql.Date EndDate;
        if(StudentID.equals("")||FullName.equals("")||jtxtDOB.getText().equals("")||jtxtStartDate.getText().equals("")
                ||jtxtEndDate.getText().equals(""))
                            {JOptionPane.showMessageDialog(null,"Required Fields are Empty");return ;}
        try{
            java.util.Date utilDate=new SimpleDateFormat("yyyy-MM-dd").parse( jtxtDOB.getText());
            DOB=new java.sql.Date(utilDate.getTime());
            utilDate=new SimpleDateFormat("yyyy-MM-dd").parse(jtxtStartDate.getText());
            StartDate=new java.sql.Date(utilDate.getTime());
            utilDate=new SimpleDateFormat("yyyy-MM-dd").parse(jtxtEndDate.getText());
            EndDate=new java.sql.Date(utilDate.getTime());
            String sql="INSERT INTO ROOT.STUDENT (SCHOLARNO,STUDENTNAME,GENDER,BRANCH,SEMESTER,DOB,MEM_START,MEM_END)"
            + " VALUES ('"+StudentID+"','"+FullName+"','"+Gender+"',"
            + "'"+Branch+"',"+Semester+",'"+DOB+"','"+StartDate+"','"+EndDate+"')";
            pst=conn.prepareStatement(sql);
            pst.executeUpdate();
            jtxtStudentID.setText("");
            jtxtStudentFullName.setText("");
            jtxtDOB.setText("");
            jtxtStartDate.setText("");
            jtxtEndDate.setText("");
            jrdToday.setSelected(false);
            jrdYear.setSelected(false);
            jrdMonth.setSelected(false);
            JOptionPane.showMessageDialog(null,"Student entry made in database");
        }catch(ParseException P){
            JOptionPane.showMessageDialog(null,P);
        } catch (SQLException ex) {
            Logger.getLogger(NewStudent.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jbtnCreateStudentActionPerformed

