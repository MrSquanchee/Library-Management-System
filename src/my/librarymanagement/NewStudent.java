    private void jrdMonthActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jrdMonthActionPerformed
        if(jrdMonth.isSelected()){
            jrdYear.setSelected(false);
            if(jtxtStartDate.getText().equals("")){JOptionPane.showMessageDialog(null,"Start Membership Field Empty"); return;}
            Calendar c=Calendar.getInstance();
            c.setTime(new java.util.Date());
            c.add(Calendar.MONTH,6);
            java.sql.Date End=new java.sql.Date(c.getTime().getTime());
            jtxtEndDate.setText(""+End);
        }else{
            jtxtEndDate.setText("");
        }
    }//GEN-LAST:event_jrdMonthActionPerformed

